package com.agile.bowlingGameKata;

public class BowlingGameFrame {

    private Integer frame;

    private int scoreOfRollOne;

    private int scoreOfRollTwo;

    private int scoreOfRollThreeForLastFrame;

    private int totalScore;

    public Integer getFrame() {
        return frame;
    }

    public void setFrame(Integer frame) {
        this.frame = frame;
    }

    public int getScoreOfRollOne() {
        return scoreOfRollOne;
    }

    public void setScoreOfRollOne(int scoreOfRollOne) {
        this.scoreOfRollOne = scoreOfRollOne;
    }

    public int getScoreOfRollTwo() {
        return scoreOfRollTwo;
    }

    public void setScoreOfRollTwo(int scoreOfRollTwo) {
        this.scoreOfRollTwo = scoreOfRollTwo;
    }

    public int getTotalScore() {
        return totalScore;
    }

    public void setTotalScore(int totalScore) {
        this.totalScore = totalScore;
    }

    public int getScoreOfRollThreeForLastFrame() {
        return scoreOfRollThreeForLastFrame;
    }

    public void setScoreOfRollThreeForLastFrame(int scoreOfRollThreeForLastFrame) {
        this.scoreOfRollThreeForLastFrame = scoreOfRollThreeForLastFrame;
    }

    

}
